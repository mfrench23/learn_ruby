class Fixnum
  def in_words
    if self == 0 
      "zero"
    elsif self == 1
      "one"
    elsif self == 2
      "two"
    elsif self == 3
      "three"
    elsif self == 4
      "four"
    elsif self == 5
      "five"
    elsif self == 6
      "six"
    elsif self == 7
      "seven"
    elsif self == 8
      "eight"
    elsif self == 9
      "nine"
    end
  end
end